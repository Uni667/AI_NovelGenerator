from __future__ import annotations


def get_platform_chapter_guidance(platform: str) -> tuple[str, str]:
    platform_key = (platform or "tomato").strip().lower()

    if platform_key == "tomato":
        return (
            "番茄小说",
            "\n".join([
                "- 开篇优先强冲突、强情绪、强悬念，前200字避免大段背景说明。",
                "- 场景要短平快，尽快出现威胁、误会、秘密、代价或欲望目标。",
                "- 对话尽量带火药味，人物关系要有拉扯，不要平铺直叙解释设定。",
                "- 每章至少有一个明显刺激点：打脸、反转、危机、揭秘、升级、选择其一。",
                "- 结尾必须留下追读钩子，让读者自然想点下一章。",
            ]),
        )

    if platform_key == "qidian":
        return (
            "起点中文网",
            "\n".join([
                "- 世界观、规则、成长路径要清楚，但不要写成说明书，要嵌进冲突里。",
                "- 强调主角行动逻辑、能力成长、资源获取与阶段性目标推进。",
                "- 爽点可以有，但要服务设定展开和长期主线，而不是只靠瞬时情绪刺激。",
                "- 信息密度要高，尽量保证每一场戏都带来新信息或新变量。",
                "- 结尾保留阶段性悬念，优先体现下一步行动目标或更大危机。",
            ]),
        )

    if platform_key == "zongheng":
        return (
            "纵横中文网",
            "\n".join([
                "- 强调主线推进、人物立场与局势变化，避免松散片段拼接。",
                "- 冲突与情绪要明确，但整体气质比纯信息流更稳，场景要有推进感。",
                "- 主角的判断、取舍、压迫感与反制手段要写清楚。",
                "- 设定展开要围绕权力、资源、阵营、命运博弈等主干来写。",
                "- 章节结尾留钩子，但同时要让本章本身形成一个完整推进单元。",
            ]),
        )

    return (
        "通用网文平台",
        "\n".join([
            "- 尽快进入冲突或悬念，不要长时间铺垫背景。",
            "- 章节内容要高信息密度，每一段都尽量服务剧情推进。",
            "- 人物关系、欲望目标、代价与选择要明确可感。",
            "- 重要场景里优先写可感知的动作、对话和变化，少空泛解释。",
            "- 结尾要留下能驱动下一章的明确悬念或未解问题。",
        ]),
    )


def get_platform_story_rhythm_guidance(platform: str) -> tuple[str, str]:
    platform_key = (platform or "tomato").strip().lower()

    if platform_key == "tomato":
        return (
            "番茄小说",
            "\n".join([
                "- 节奏要前置刺激点，前三章就要把核心卖点、核心冲突、核心钩子打出来。",
                "- 单章推进要快，少做长铺垫，优先让人物在冲突中暴露关系、秘密和欲望。",
                "- 每3-5章形成一个强追读单元，单元结尾要比单章结尾更狠。",
                "- 爽点、虐点、误会、反转、危机要高频切换，避免连续平缓章节。",
                "- 目录标题和章节简述要尽量直给，读者一眼能看懂本章刺激点。",
            ]),
        )

    if platform_key == "qidian":
        return (
            "起点中文网",
            "\n".join([
                "- 节奏要围绕主线成长、能力提升、规则展开、资源争夺来组织。",
                "- 前期要尽快立住设定与成长目标，但允许比信息流平台多一点世界观铺设。",
                "- 单元高潮要兼顾事件爆发与体系升级，不能只靠情绪反转。",
                "- 中长线伏笔要稳定推进，目录和架构要服务长期追读而非只看即时刺激。",
                "- 章节标题与简述应明确体现推进目标、冲突对象或新变量。",
            ]),
        )

    if platform_key == "zongheng":
        return (
            "纵横中文网",
            "\n".join([
                "- 节奏要强调局势变化、阵营博弈、人物立场推进，章节之间要有明显承接。",
                "- 高潮不仅是情绪爆发，也要体现局面翻转、筹码变化与选择代价。",
                "- 目录和章节简述要体现主线推进，不宜过度碎片化或纯标题党。",
                "- 允许保留一定沉浸和压迫感，但每章都要推进一格局势。",
                "- 单元结尾优先制造更大棋局、身份暴露、利益冲突升级等类型钩子。",
            ]),
        )

    return (
        "通用网文平台",
        "\n".join([
            "- 前期尽快亮出卖点和冲突，中期持续升级，后期集中回收。",
            "- 每个章节单元都要兼顾推进、悬念和情绪，不要长时间停在说明状态。",
            "- 目录标题和章节简述要让读者快速理解本章价值。",
            "- 重要钩子要分布均匀，避免前密后松或连续平章。",
        ]),
    )
